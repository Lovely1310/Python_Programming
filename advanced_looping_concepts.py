# # Dictionaries Comprehension
# new_dict = {key_expression: value_expression for item in iterable if condition}
n = [1,2,3,4,5,6,7]
sq = {x : x**2 for x in n}
print(sq)

even_dict = {x : x**2 for x in range(10) if x%2==0 }
print(even_dict)

