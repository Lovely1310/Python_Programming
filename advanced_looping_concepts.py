# lambda function - A lambda function is an anonymous function that can have multiple arguments but only one expression. syntax :- lambda arguments : expression
sq = lambda x : x**2
print(sq(4))

add = lambda x,y : x+y
print(add(5,5))

numbers = [1, 2, 3, 4]
squared_numbers = list(map(lambda x: x ** 2, numbers))
print(squared_numbers)

even_numbers = list(filter(lambda x: x % 2 == 0, range(10)))
print(even_numbers)
